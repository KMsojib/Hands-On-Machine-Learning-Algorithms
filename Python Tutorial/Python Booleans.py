def main():
    # Boolean values
    a = True
    b = False

    # Print the boolean values
    print("Value of a:", a)
    print("Value of b:", b)

    # Check if a is True
    if a:
        print("a is True")
    else:
        print("a is False")

    # Check if b is False
    if not b:
        print("b is False")
    else:
        print("b is True")
        
    # Greater than comparison
    a = 10
    b = 100
    
    if a > b:
        print("a is greater than b")
    else:
        print("a is not greater than b")
    
    # Less than comparison
    if a < b:
        print("a is less than b")
    else:
        print("a is not less than b")
        
        
      
if __name__ == "__main__":
    main()